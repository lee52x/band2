package com.band.manager.interest;

public class Interest {
	
	private String hashName, hashDate;
	private int hashNum;
	private int hashCount, listNum;

	
	public int getListNum() {
		return listNum;
	}

	public void setListNum(int listNum) {
		this.listNum = listNum;
	}

	public int getHashCount() {
		return hashCount;
	}

	public void setHashCount(int hashCount) {
		this.hashCount = hashCount;
	}
	
	public String getHashDate() {
		return hashDate;
	}

	public void setHashDate(String hashDate) {
		this.hashDate = hashDate;
	}

	public int getHashNum() {
		return hashNum;
	}

	public void setHashNum(int hashNum) {
		this.hashNum = hashNum;
	}

	public String getHashName() {
		return hashName;
	}

	public void setHashName(String hashName) {
		this.hashName = hashName;
	}
	
}
